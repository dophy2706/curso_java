
public class Constantes {

	public static void main(String[] args) {
		
		/* Estructura de una constante:
		 * Su estructura está compuesta por:

			final [tipodedato] [nombredelaconstante]; //Declaración

			final [tipodedato] [nombredelaconstante] = 10; //Declaración + iniciación
		 */
		
		
    	final byte a = 1;
    	System.out.println(a);
    	
    	final short b = 2;
    	System.out.println(b);
    	
    	final int c = 3;
    	System.out.println(c);
    	
    	final long d = 4L; //OJO acabado en L
    	System.out.println(d);
    	
    	final float e = 1.253F; //OJO acabada en F y con los decimales en . NO EN COMA!
    	System.out.println(e);
    	
    	final double f = 2.25132514;
    	System.out.println(f);
    	
    	final boolean g = true;
    	System.out.println(g);
    	
    	final char h = 'C'; //OJO entre comillas simples
    	System.out.println(h);
		
	}

}
